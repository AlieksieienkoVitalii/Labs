myList = [1, 4, 7, 54, 8, 9, 234, 78, 35]
print("Значения моего списка - " + str(myList))
print("Максимальное число в моем списке - " + str(max(myList)))
print("Минимальное число в моем списке - " + str(min(myList)))
print("Сумма чисел в моем списке - " + str(sum(myList)))
print("Среднее значение моего списка - " + str(sum(myList) / len(myList)))

